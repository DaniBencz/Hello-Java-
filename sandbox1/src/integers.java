public class integers {
    public static void integers1() {
        // int 32 bits
        int a, c, d;
        a = 10;
        int b = 20;
        c = a * b;
        System.out.println(c);

        /*
        byte: 8 bits
        short: -128 to 128
        long: 64 bits
        */
        float decimal;
        decimal = 1.2f;
        System.out.println(decimal);
        //double: large float, no f
        char e = '1';
        System.out.println(e);
    }
}
